import streamlit as st
import numpy as np
import pandas as pd

st.set_page_config(page_title="MIND: Making Inference Digestible", layout="centered")

st.title("üß† MIND: Making Inference Digestible")
st.subheader("üéØ Interactive Probability & Statistics Explorer")

st.markdown("""
Welcome to MIND, an interactive app designed to help you understand **discrete probability distributions**.
Enter values for a random variable and corresponding probabilities, and visualize the distribution live.
""")

# Collect input
x_input = st.text_input("Enter values for random variable X (comma-separated):", "0, 1, 2, 3")
p_input = st.text_input("Enter corresponding probabilities P(X) (fractions or decimals, comma-separated):", "0.1, 0.3, 0.4, 0.2")

try:
    x_vals = [float(x.strip()) for x in x_input.split(',')]
    p_vals = [eval(p.strip()) for p in p_input.split(',')]  # Supports fractions
    assert np.isclose(sum(p_vals), 1), "Probabilities must sum to 1."

    df = pd.DataFrame({'X': x_vals, 'P(X)': p_vals})
    df['Cumulative P(X)'] = df['P(X)'].cumsum()
    st.dataframe(df)

    st.bar_chart(df.set_index("X")["P(X)"])

    st.success("‚úÖ Valid distribution!")
except Exception as e:
    st.error(f"‚ùå Error: {e}")